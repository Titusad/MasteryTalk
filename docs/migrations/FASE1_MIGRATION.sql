-- ═══════════════════════════════════════════════════════════
--  TABLE 1: profiles
--  Blueprint §3.1 — Auth & Metadata
--  Links 1:1 with auth.users via id FK
-- ═══════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.profiles (
  id               uuid PRIMARY KEY REFERENCES auth.users ON DELETE CASCADE,
  market_focus     text CHECK (market_focus IN ('mexico', 'colombia', 'brazil')),
  plan             text DEFAULT 'free'
                     CHECK (plan IN ('free', 'per-session')),
  plan_status      text DEFAULT 'active'
                     CHECK (plan_status IN ('active', 'trial', 'expired')),
  free_session_used boolean DEFAULT false,
  stats            jsonb DEFAULT '{}'::jsonb,
    -- Cache: { "sessions_count": 0, "avg_score": 0, "power_phrases_count": 0 }
  achievements     text[] DEFAULT '{}',
    -- Misiones completadas: ["The 24-Hour Pulse", "Mirror Match", etc.]
  created_at       timestamptz DEFAULT now()
);

COMMENT ON TABLE  public.profiles IS 'User profile — 1:1 with auth.users. Contains plan, stats cache, achievements.';
COMMENT ON COLUMN public.profiles.stats IS 'JSON cache: sessions_count, avg_score, power_phrases_count. Updated by Edge Functions.';
COMMENT ON COLUMN public.profiles.market_focus IS 'mexico, colombia, or brazil — determines regional vocabulary in prompts and cultural directives in analyst.';

-- ═══════════════════════════════════════════════════════════
--  TABLE 2: sessions
--  Blueprint §3.2 — Practice sessions with conversation history
--  NOTE: history as JSONB is documented technical debt (-> V4.0)
-- ═══════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.sessions (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id          uuid REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  scenario_config  jsonb NOT NULL,
    -- { "scenario": "...", "interlocutor": "client|manager|recruiter|peer",
    --   "scenarioType": "interview|sales", "market_focus": "mexico|colombia|brazil" }
  system_prompt    text,
    -- Full assembled prompt generated by Edge Function prepare-session
  voice_id         text,
    -- ElevenLabs voice_id assigned by prepare-session based on interlocutor
  scenario_type    text CHECK (scenario_type IN ('interview', 'sales')),
    -- MVP scenario types. Future: 'csuite', 'negotiation', 'networking'
    -- (adaptations exist in assembler.ts but UI doesn't expose them yet)
  history          jsonb DEFAULT '[]'::jsonb,
    -- Array of { role, content, audio_url, internal_analysis? }
    -- internal_analysis is BACKEND-ONLY — never sent to frontend
  feedback         jsonb,
    -- { strengths: [...], opportunities: [...], improved_script: {...} }
  arena_phase_reached text CHECK (arena_phase_reached IN ('support', 'guidance', 'challenge')),
    -- Highest arena phase the user reached during this session
    -- Maps to ArenaPhase type in types.ts
  mindset_pulse    jsonb,
    -- Post-session self-assessment: { confidenceLevel: 1-5, biggestFear: "...",
    --   selfAssessment: 1-5, completedAt: "ISO date" }
    -- Maps to MindsetPulseResult type in types.ts
  status           text DEFAULT 'active'
                     CHECK (status IN ('active', 'completed')),
  turn_count       int DEFAULT 0,
    -- Counter for isComplete logic (4-8 turn range)
  created_at       timestamptz DEFAULT now()
);

COMMENT ON TABLE  public.sessions IS 'Practice sessions. history JSONB is tech debt — migrate to session_messages in V4.0.';
COMMENT ON COLUMN public.sessions.history IS 'JSONB array. AI entries include internal_analysis (backend-only, never exposed to frontend).';
COMMENT ON COLUMN public.sessions.scenario_type IS 'MVP: interview|sales. Future types prepared in assembler.ts (csuite, negotiation, networking).';
COMMENT ON COLUMN public.sessions.arena_phase_reached IS 'Progressive scaffolding: support -> guidance -> challenge. Logged for practice history.';

-- ═══════════════════════════════════════════════════════════
--  TABLE 3: sr_cards (Spaced Repetition)
--  Blueprint §3.3 — Cards with 4-step interval system
--  Intervals: 24h -> 3d -> 7d -> 14d
-- ═══════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.sr_cards (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id          uuid REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  session_id       uuid REFERENCES public.sessions(id) ON DELETE SET NULL,
    -- Source session that generated this card (nullable for manual adds)
  phrase           text NOT NULL,
  problem_word     text,
    -- The specific word that needs work (shown on card)
  phonetic         text,
    -- IPA phonetic transcription of the problem word
  interval_step    int DEFAULT 1
                     CHECK (interval_step BETWEEN 1 AND 4),
    -- 1=24h, 2=3d, 3=7d, 4=14d (graduated)
  next_review_at   timestamptz DEFAULT now(),
  last_score       int DEFAULT 0,
  created_at       timestamptz DEFAULT now()
);

COMMENT ON TABLE  public.sr_cards IS 'Spaced Repetition cards. interval_step 1-4 maps to 24h/3d/7d/14d review cycle.';

-- ═══════════════════════════════════════════════════════════
--  TABLE 4: power_phrases
--  Blueprint §3.4 — Saved phrases with category & context
-- ═══════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.power_phrases (
  id                uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id           uuid REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  phrase            text NOT NULL,
  category          text,
    -- "Negotiation", "Softening", "Closing", "Persuasion", etc.
  context_usage     text,
    -- When to use it (human-readable description)
  source_session_id uuid REFERENCES public.sessions(id) ON DELETE SET NULL,
  created_at        timestamptz DEFAULT now()
);

COMMENT ON TABLE public.power_phrases IS 'User-saved power phrases from practice sessions, with category and usage context.';

-- ═══════════════════════════════════════════════════════════
--  TABLE 5: audit_logs (Financial Control)
--  Blueprint §3.5 — Cost tracking for $12-$14 margin monitoring
--  Every Edge Function call logs here automatically
-- ═══════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.audit_logs (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  function_name    text NOT NULL,
    -- prepare-session, process-turn, analyze-feedback, generate-script,
    -- generate-results-summary, score-pronunciation, tts-generate
  user_id          uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  session_id       uuid REFERENCES public.sessions(id) ON DELETE SET NULL,
  tokens_in        int DEFAULT 0,
  tokens_out       int DEFAULT 0,
  elevenlabs_chars int DEFAULT 0,
    -- Characters sent to ElevenLabs TTS (for billing tracking)
  model            text,
    -- gpt-4o, gpt-4o-mini, gemini-1.5-flash, azure-tts, elevenlabs, etc.
  latency_ms       int DEFAULT 0,
  cost_estimated   numeric(10,4) DEFAULT 0,
    -- USD cost estimated by the Edge Function at call time
  error_code       text,
    -- NULL on success, error code on failure (for debugging)
  created_at       timestamptz DEFAULT now()
);

COMMENT ON TABLE  public.audit_logs IS 'Financial control — every AI/TTS call is logged with cost. Critical for $12-$14/user margin.';
COMMENT ON COLUMN public.audit_logs.cost_estimated IS 'USD estimated cost. Calculated in Edge Function using token counts * model pricing.';

