-- ═══════════════════════════════════════════════════════════
--  TRIGGER: Auto-create profile on user registration
--  QA Test: F1-12
--
--  When auth.users gets a new row (Google OAuth),
--  this trigger automatically creates the profiles row
--  with default values. No manual INSERT needed.
-- ═══════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  INSERT INTO public.profiles (id, market_focus, stats)
  VALUES (
    NEW.id,
    -- Default market_focus — can be updated later via onboarding or IP detection
    'mexico',
    -- Initialize stats cache with zeros
    '{"sessions_count": 0, "avg_score": 0, "power_phrases_count": 0}'::jsonb
  );
  RETURN NEW;
END;
$$;

-- Drop existing trigger if re-running migration
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_new_user();

-- ═══════════════════════════════════════════════════════════
--  ROW LEVEL SECURITY (RLS)
--  QA Tests: F1-10, F1-11
--
--  Principle: Every user can ONLY access their own data.
--  Service role (Edge Functions) bypasses RLS for admin ops.
-- ═══════════════════════════════════════════════════════════

-- --- profiles ---
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read own profile"
  ON public.profiles FOR SELECT
  USING (auth.uid() = id);

CREATE POLICY "Users can update own profile"
  ON public.profiles FOR UPDATE
  USING (auth.uid() = id)
  WITH CHECK (auth.uid() = id);

-- Note: INSERT is handled by the trigger (SECURITY DEFINER).
-- Users should NOT be able to INSERT profiles directly.
-- DELETE is intentionally omitted (account deletion via admin/support).

-- --- sessions ---
ALTER TABLE public.sessions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read own sessions"
  ON public.sessions FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own sessions"
  ON public.sessions FOR INSERT
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update own sessions"
  ON public.sessions FOR UPDATE
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

-- --- sr_cards ---
ALTER TABLE public.sr_cards ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read own SR cards"
  ON public.sr_cards FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own SR cards"
  ON public.sr_cards FOR INSERT
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update own SR cards"
  ON public.sr_cards FOR UPDATE
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

-- --- power_phrases ---
ALTER TABLE public.power_phrases ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read own power phrases"
  ON public.power_phrases FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own power phrases"
  ON public.power_phrases FOR INSERT
  WITH CHECK (auth.uid() = user_id);

